    private void btn_submit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submit1ActionPerformed
//         TODO add your handling code here:
        try {
            String newpath = "src/upload";
            File directory = new File(newpath);
            if (!directory.exists()) {
                directory.mkdirs();
            }

            File fileawal = null;
            File fileakhir = null;
            String ext = this.filename.substring(filename.lastIndexOf('.')+1);
            fileawal = new File(filename);
            fileakhir = new File(newpath+"/"+jTextField_lokasi.getText()+"."+ext);
            System.out.println(fileakhir);

            catatan c = new catatan();
            c.setTanggal(new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser_tgl.getDate()));
            c.setLokasi(jTextField_lokasi.getText());
            c.setDeskripsi(jTextArea_deskripsi.getText());
            c.setFoto_lokasi(fileakhir.toString());

            PreparedStatement stat = null;
            if (btn_submit1.getText().equals("Tambah")) {
                stat = db.getCon().prepareStatement("INSERT INTO catatan(nik, nama, tanggal, lokasi, deskripsi, foto_lokasi) VALUES(?,?,?,?,?,?)");
                stat.setString(1, this.nik);
                stat.setString(2, this.nama);
                stat.setString(3, c.getTanggal());
                stat.setString(4, c.getLokasi());
                stat.setString(5, c.getDeskripsi());
                stat.setString(6, c.getFoto_lokasi());
            } else if (btn_submit1.getText().equals("Ubah")) {
                stat = db.getCon().prepareStatement("UPDATE catatan SET tanggal=?, lokasi=?, deskripsi=?, foto_lokasi=? WHERE id_catatan=?");
                stat.setString(1, c.getTanggal());
                stat.setString(2, c.getLokasi());
                stat.setString(3, c.getDeskripsi());
                stat.setString(4, c.getFoto_lokasi());
                stat.setString(5, lbl_id.getText());
            }
            stat.executeUpdate();

            if (fileawal.exists()) {
                Files.copy(fileawal.toPath(), fileakhir.toPath(), StandardCopyOption.REPLACE_EXISTING);
            }

            setPanel("tanggal");
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            jTabbedPane1.setSelectedIndex(1);
            lbl_id.setText("Proses Data");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_submit1ActionPerformed

    private void btn_pilih_fotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pilih_fotoActionPerformed
        // TODO add your handling code here:
        catatan c = new catatan();
        if (jDateChooser_tgl.equals(null) || jTextField_lokasi.getText().equals("") || jTextArea_deskripsi.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "tanggal, lokasi dan deskripsi tidak Boleh Kosong");
        }else{
            try {
                JFileChooser chooser = new JFileChooser();//untuk pop up pilih file
                chooser.showOpenDialog(null);
                File f = chooser.getSelectedFile();//isi file yang dipilih
                ImageIcon icon = new ImageIcon(f.toString());//membuat image agar bisa ditampilkan di label
                //sesuaikan image dengan dimensi label
                Image img = icon.getImage().getScaledInstance(label_foto_lokasi.getWidth(), label_foto_lokasi.getHeight(), Image.SCALE_DEFAULT);;
                ImageIcon ic = new ImageIcon(img);
                label_foto_lokasi.setIcon(ic);
                this.filename = f.getAbsolutePath();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_btn_pilih_fotoActionPerformed

