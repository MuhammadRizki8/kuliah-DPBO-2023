    public void setPanel(String urutan){
        panel_data.removeAll();
        panel_data.setLayout(new GridLayout(0, 1));
        try {
            this.stat = db.getCon().prepareStatement("select * from catatan where " + "nik='"+this.nik+"' order by `catatan`.`"+urutan+"` asc");
            this.rs= this.stat.executeQuery();
            while (rs.next()) {
                String tanggal = rs.getString("tanggal");
                String lokasi = rs.getString("lokasi");
                String deskripsi = rs.getString("deskripsi");
                String fotoLokasi = rs.getString("foto_lokasi");
                int id=rs.getInt("id_catatan");
                panel_data.add(new Card(tanggal, lokasi, deskripsi, fotoLokasi,id ));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        panel_data.revalidate();
        panel_data.repaint();
        
        this.clear_form_input();
    } 
